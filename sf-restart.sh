#!/bin/bash

# ==============================================================================
# sf-restart.sh - 接続組織の切り替えスクリプト
# ==============================================================================
# ローカルの接続設定をクリアし、FORCE_RELOGIN=1 で sf-start.sh を呼び出すことで
# 別の Salesforce 組織への強制再ログインを行います。
#
# 【オプション】
#   -v, --verbose       : コマンドの応答（出力）をコンソールにも表示します
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 共通ライブラリの必須設定
# ------------------------------------------------------------------------------
readonly SCRIPT_NAME=$(basename "$0" .sh)
readonly LOG_FILE="./sf-tools/logs/${SCRIPT_NAME}.log"
readonly LOG_MODE="NEW"


# ------------------------------------------------------------------------------
# 2. 共通ライブラリの読み込み
# ------------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
COMMON_LIB="${SCRIPT_DIR}/lib/common.sh"

if [[ ! -f "$COMMON_LIB" ]]; then
    echo "[FATAL ERROR] Library not found: $COMMON_LIB" >&2
    exit 1
fi
source "$COMMON_LIB"

# ------------------------------------------------------------------------------
# 3. 初期チェック
# ------------------------------------------------------------------------------
log "HEADER" "接続組織の切り替え（Switch）を開始します (${SCRIPT_NAME}.sh)"

# ------------------------------------------------------------------------------
# 4. ローカルの接続設定をクリア
# ------------------------------------------------------------------------------
# .sf/config.json と .sfdx/sfdx-config.json を削除することで、
# sf-start.sh 内の「接続済み自動スキップ」を無効化する
log "INFO" "現在のローカル接続設定をクリアしています..."
run rm -f .sf/config.json .sfdx/sfdx-config.json

# ------------------------------------------------------------------------------
# 5. 強制再ログインフラグを立てて sf-start.sh を呼び出す
# ------------------------------------------------------------------------------
[ -f "./sf-start.sh" ] || die "sf-start.sh が見つかりません。同じディレクトリに配置してください。"

FORCE_RELOGIN=1 bash "./sf-start.sh" "$@"
exit $?
