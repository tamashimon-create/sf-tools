#!/bin/bash

# ==============================================================================
# sf-update.sh - 開発ツール一括アップデートスクリプト
# ==============================================================================
# Git / Salesforce CLI を最新バージョンにアップデートします。
# ※ Node.js は Windows インストーラーで手動アップデートしてください。
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 共通ライブラリの必須設定
# ------------------------------------------------------------------------------
readonly SCRIPT_NAME=$(basename "$0" .sh)
readonly LOG_FILE="./logs/${SCRIPT_NAME}.log"
readonly LOG_MODE="NEW"
readonly SILENT_EXEC=0

# ------------------------------------------------------------------------------
# 2. 共通ライブラリの読み込み
# ------------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
COMMON_LIB="${SCRIPT_DIR}/lib/common.sh"

if [[ ! -f "$COMMON_LIB" ]]; then
    echo "[FATAL ERROR] Library not found: $COMMON_LIB" >&2
    exit 1
fi

source "$COMMON_LIB"

# ==============================================================================
# メイン処理
# ==============================================================================
log INFO "開発ツールのアップデートを開始します"

# ------------------------------------------------------------------------------
# Git のアップデート
# ------------------------------------------------------------------------------
log INFO "Git をアップデートします..."
run git update-git-for-windows
log SUCCESS "Git のアップデートが完了しました"

# ------------------------------------------------------------------------------
# Salesforce CLI のアップデート
# ------------------------------------------------------------------------------
log INFO "Salesforce CLI をアップデートします..."
run sf update --no-prompt
log SUCCESS "Salesforce CLI のアップデートが完了しました"

log SUCCESS "すべてのアップデートが完了しました"
