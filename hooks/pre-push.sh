#!/bin/bash

# ==============================================================================
# プログラム名: pre-push (Gitフックの実体)
# 役割: `git push` コマンド実行時に、自動的にSalesforce組織への検証(Dry-Run)を
#       実行し、エラーがあればPushを未然に防ぐ安全装置。
# 設置場所: ~/sf-tools/hooks/pre-push
# 呼び出し元: 各プロジェクトの .git/hooks/pre-push (sf-hook.sh により生成)
# ==============================================================================

# ------------------------------------------------------------------------------
# 0. 共通の初期処理
# ------------------------------------------------------------------------------
# ターミナル出力用のカラー定義
# 実行環境がインタラクティブなターミナルの場合のみ色を有効化し、ログファイル等を汚さないようにする
if [ -t 1 ]; then
    readonly CLR_INFO='\033[36m'
    readonly CLR_SUCCESS='\033[32m'
    readonly CLR_ERR='\033[31m'
    readonly CLR_PROMPT='\033[33m'
    readonly CLR_RESET='\033[0m'
else
    readonly CLR_INFO=''; readonly CLR_SUCCESS=''; readonly CLR_ERR=''; readonly CLR_PROMPT=''; readonly CLR_RESET=''
fi

# Gitクライアントのコンソール出力を汚さないよう、進捗メッセージはすべて標準エラー出力(>&2)へ送る
echo "=======================================================" >&2
echo -e "${CLR_INFO}▶️  [PRE-PUSH] Salesforce 組織への検証(Dry-Run)を自動開始します...${CLR_RESET}" >&2
echo "=======================================================" >&2

# 実行ディレクトリのバリデーション
# プロジェクトルート（force-から始まるディレクトリ）以外での誤実行による事故を防止します
CURRENT_DIR_NAME=$(basename "$PWD")
if [[ ! "$CURRENT_DIR_NAME" =~ ^force- ]]; then
    echo -e "${CLR_ERR}❌ エラー: このスクリプトは 'force-*' ディレクトリ内でのみ実行可能です。${CLR_RESET}" >&2
    exit 1
fi

# ------------------------------------------------------------------------------
# 1. 検証の実行
# ------------------------------------------------------------------------------
# このフックの心臓部である、リリース/検証スクリプトのパスを定義
readonly SCRIPT_PATH="$HOME/sf-tools/sf-release.sh"

# 【安全性】sf-release.sh が存在しない場合は、エラーを通知して処理を中断する
if [ ! -f "$SCRIPT_PATH" ]; then
    echo -e "${CLR_ERR}❌ [PRE-PUSH] ツールが見つかりません: $SCRIPT_PATH ${CLR_RESET}" >&2
    exit 1
fi

# sf-release.sh を実行。引数を何も指定しないことで、デフォルトの「検証モード」で動作させる。
# これにより、実際のデプロイは行われず、安全なDry-Runのみが実行される。
"$SCRIPT_PATH"
RESULT=$?

# ------------------------------------------------------------------------------
# 2. 結果判定
# ------------------------------------------------------------------------------
# sf-release.sh の終了コードを基に、git push を続行するか中断するかを決定する
echo "=======================================================" >&2
if [ $RESULT -eq 0 ]; then
    # 終了コードが 0 (成功) の場合、検証が成功したとみなし、このフックスクリプトも 0 で終了する。
    # これにより、git push のプロセスが正常に続行される。
    echo -e "${CLR_SUCCESS}✅ [PRE-PUSH] 検証成功！リモートリポジトリへ Push します。${CLR_RESET}" >&2
    exit 0
else
    # 終了コードが 0 以外 (失敗) の場合、検証でエラーが発生したとみなし、1 で終了する。
    # これにより、git push のプロセスが中断され、問題のあるコードがリモートに反映されるのを防ぐ。
    echo -e "${CLR_ERR}❌ [PRE-PUSH] 組織の検証でエラーが発生したため、Push を中断しました。${CLR_RESET}" >&2
    echo -e "コンポーネントのエラー詳細は ./logs/sf-release.log を確認してください。" >&2
    exit 1
fi