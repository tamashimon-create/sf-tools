#!/bin/bash

# ==============================================================================
# プログラム名: sf-startswitch.sh
# 概要: 現在の接続設定を初期化し、別の組織への切り替え（強制再認証）を行う
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 共通ライブラリの必須設定
# ------------------------------------------------------------------------------
readonly SCRIPT_NAME=$(basename "$0" .sh)
readonly LOG_FILE="./logs/${SCRIPT_NAME}.log"
readonly LOG_MODE="NEW"         # 実行のたびにログをリセット
readonly SILENT_EXEC=1          # コマンドの標準出力はログファイルのみに記録

# ------------------------------------------------------------------------------
# 2. 共通ライブラリの読み込み
# ------------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
COMMON_LIB="${SCRIPT_DIR}/lib/common.sh"

if [[ ! -f "$COMMON_LIB" ]]; then
    echo "[FATAL ERROR] Library not found: $COMMON_LIB" >&2
    exit 1
fi
source "$COMMON_LIB"

# ------------------------------------------------------------------------------
# 3. 初期チェック
# ------------------------------------------------------------------------------
# プロジェクトディレクトリ（force-で始まる）にいるか確認
check_force_dir || die "このスクリプトは 'force-*' ディレクトリ内で実行してください。"

log "HEADER" "" "接続組織の切り替え（Switch）を開始します..."

# ------------------------------------------------------------------------------
# 1. ローカルの接続設定ファイルを物理的に削除
# ------------------------------------------------------------------------------
# これにより、sf-start.sh 内の「接続済み自動スキップ」を無効化します
log "INFO" "INIT" "現在のローカル接続設定をクリアしています..."
run "SYNC" rm -f .sf/config.json .sfdx/sfdx-config.json
    
# ------------------------------------------------------------------------------
# 2. 強制ログインフラグ(FORCE_RELOGIN)を立ててメインスクリプトを呼び出す
# ------------------------------------------------------------------------------
if [ -f "./sf-start.sh" ]; then
    FORCE_RELOGIN=1 bash "./sf-start.sh"
else
    echo -e "${CLR_ERR}❌ sf-start.sh が見つかりません。同じディレクトリに配置してください。${CLR_RESET}"
    exit 1
fi

exit 0