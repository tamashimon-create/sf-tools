#!/bin/bash
# ==============================================================================
# sf-mergecheck.sh - プッシュ前 main 同期チェックスクリプト
# ==============================================================================
# git push 実行前に main ブランチとの同期状態を検証します。
#
# 【検証内容】
#   - main ブランチへの直接プッシュを禁止
#   - リモート main に未取り込みコミットがある場合はプッシュを中断
#
# 【オプション】
#   -v, --verbose       : コマンドの応答（出力）をコンソールにも表示します
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 共通ライブラリの必須設定
# ------------------------------------------------------------------------------
readonly SCRIPT_NAME=$(basename "$0" .sh)
readonly LOG_FILE="./sf-tools/logs/${SCRIPT_NAME}.log"
readonly LOG_MODE="APPEND"


# ------------------------------------------------------------------------------
# 2. 共通ライブラリの読み込み
# ------------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
COMMON_LIB="${SCRIPT_DIR}/lib/common.sh"

if [[ ! -f "$COMMON_LIB" ]]; then
    echo "[FATAL ERROR] Library not found: $COMMON_LIB" >&2
    exit 1
fi
source "$COMMON_LIB"

# ------------------------------------------------------------------------------
# 3. 初期チェック
# ------------------------------------------------------------------------------
log "HEADER" "main 同期チェックを開始します (${SCRIPT_NAME}.sh)"

# ------------------------------------------------------------------------------
# 4. 実行時引数の解析
# ------------------------------------------------------------------------------
while [[ "$#" -gt 0 ]]; do
    case $1 in
        --verbose|-v) : ;;  # SILENT_EXEC は common.sh が設定済み
        --*)
            die "不明なオプションです: $1"
            ;;
        *)
            die "不明な引数です: $1"
            ;;
    esac
    shift
done

# ------------------------------------------------------------------------------
# 5. フェーズ定義
# ------------------------------------------------------------------------------

# 【FETCH】リモートの最新情報を取得
phase_fetch() {
    log "INFO" "リモート(origin)の最新情報を確認中..."
    run git fetch origin main -q || return $RET_NG
    return $RET_OK
}

# 【CHECK】main ブランチとの同期を検証
phase_check() {
    local current_branch
    current_branch=$(run git symbolic-ref --short HEAD)

    # main への直接プッシュを禁止
    if [[ "$current_branch" == "main" ]]; then
        die "main ブランチへの直接プッシュは禁止されています。PR を作成してください。"
    fi

    # リモート main に未取り込みコミットがあればブロック
    local missing_commits
    missing_commits=$(run git log "${current_branch}..origin/main" --oneline)

    if [[ -n "$missing_commits" ]]; then
        log "ERROR" "リモートの main ブランチが更新されています。"
        log "INFO"  "プッシュ前に以下のコミットを取り込む必要があります:"
        echo "--------------------------------------------------"
        echo "$missing_commits"
        echo "--------------------------------------------------"
        die "'git pull origin main' を実行し、マージ（またはリベース）してください。"
    fi

    return $RET_OK
}

# ------------------------------------------------------------------------------
# 6. メイン実行フロー
# ------------------------------------------------------------------------------
phase_fetch || die "リモート情報の取得に失敗しました。"

phase_check || die "main ブランチとの同期確認に失敗しました。"
log "SUCCESS" "main ブランチと同期されています。プッシュを継続します。"

exit $RET_OK
